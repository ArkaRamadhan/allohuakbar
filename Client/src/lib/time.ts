export const Detik = (value: number) => {
  return value * 1000;
};

export const Menit = (value: number) => {
  return value * 1000 * 60;
};

export const Jam = (value: number) => {
  return value * 1000 * 60 * 60;
};

export const Hari = (value: number) => {
  return value * 1000 * 60 * 60 * 24;
};
